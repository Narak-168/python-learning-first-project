#Casting Example of Python Programming Language.

num = int('100') #Casting age to integer
print(num + 120) #Output will be 220
#---------------------------------------------------------------
age = 20
text = "Age is " + str(age) #Casting age to string
print(text) #Output will be "Age is 20"
#---------------------------------------------------------------
pi = 3.14159
print(int(pi)) #Casting pi to integer, output will be 3
#---------------------------------------------------------------
number = 10
print(float(number)) #Casting number to float, output will be 10.0
#---------------------------------------------------------------    
value = 5.5
print(int(value)) #Casting value to integer, output will be 5
#---------------------------------------------------------------    
num_str = "123"
print(int(num_str)) #Casting num_str to integer, output will be 123 
#---------------------------------------------------------------
num_float = 3.14
print(str(num_float)) #Casting num_float to string, output will be "3.14"
#---------------------------------------------------------------
num_bool = 1
print(bool(num_bool)) #Casting num_bool to boolean, output will be True